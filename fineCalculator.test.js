const calculateFine = require('./fineCalculator');
describe('Тесты функции расчета штрафа', () => {
    describe('Позитивные сценарии', () => {
        test('Должен вернуть правильный штраф за 5 дней по 2 в день', () => {
            expect(calculateFine(5, 2)).toBe(10);
        });
        test('Должен вернуть правильный штраф за 6 дней по 10 в день', () => {
            expect(calculateFine(6, 10)).toBe(60);
        });
        test('Должен вернуть правильный штраф за 2 дня по 8 в день', () => {
            expect(calculateFine(2, 8)).toBe(16);
        });
    });
    describe('Граничные значения', () => {
        test('Должен вернуть 0 при нулевой просрочке (0 дней, тариф 2)', () => {
            expect(calculateFine(0, 2)).toBe(0);
        });
        test('Должен вернуть 0 при нулевой просрочке (0 дней, тариф 5)', () => {
            expect(calculateFine(0, 5)).toBe(0);
        });
        test('Должен вернуть 0 при нулевой просрочке (0 дней, тариф 10)', () => {
            expect(calculateFine(0, 10)).toBe(0);
        });
    });
    describe('Негативные сценарии', () => {
        test('Должен выбросить ошибку при отрицательных днях', () => {
            expect(() => calculateFine(-5, 2)).toThrow('Количество дней не может быть отрицательным');
        });
        test('Должен выбросить ошибку при отрицательном тарифе', () => {
            expect(() => calculateFine(5, -2)).toThrow('Тариф не может быть нулевым или отрицательным');
        });
        test('Должен выбросить ошибку при нулевом тарифе', () => {
            expect(() => calculateFine(5, 0)).toThrow('Тариф не может быть нулевым или отрицательным');
        });
    });
});